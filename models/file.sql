CREATE TABLE registro (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(80),
    cedula VARCHAR(15),
    email VARCHAR(80),
    telefono VARCHAR(20),
    municipio VARCHAR(80),
    direccion VARCHAR(80),
    boleta VARCHAR(20),
    responsable VARCHAR(80),
    codigo VARCHAR(80),
    status VARCHAR(80),
    premio VARCHAR(80),
   
);